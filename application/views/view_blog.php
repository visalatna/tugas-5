    <p>Jenis website berdasarkan sifat

• Website Dinamis adalah sebuah website yang berisi konten yang selalu berubah setiap saat, contohnya website toko online, website internet banking, dll.
• Website Statis merupakan website yang kontennya sangat jarang diubah, contohnya website landing page.</p>

<p>Jenis website berdasarkan tujuan

• Personal Website adalah situs web yang berisi informasi pribadi seseorang.
• Corporate web, website yang dimiliki oleh perusahaan
• Portal Website adalah website yang memiliki banyak layanan berita, email dan jasa-jasa lainnya.
• Website Media Sharing, web yang bertujuan untuk berbagi media antar pengguna seperti gambar, video, musik. Contohnya: Flickr, Youtube, Soundcloud.
• Forum Website adalah website yang digunakan sebagai sarana diskusi pengunjungnya
• Selain ini terdapat ula beberapa website lain seperti website pemerintah, e-banking, e-payment, e-procurement dan sebagainya.</p>


<p>Jenis website berdasarkan bahasa pemrograman

• Server side adalah web yang menggunakan bahasa pemrograman yang tergantung pada tersedianya server seperti PHP, ASP dan sebagainya. Jika tidak ada server, maka website yang dibangun menggunakan bahasa pemrograman tidak dapat berfungsi sebagaimana mestinya.
• Client side adalah website yang tidak membutuhkan server dalam menjalankannya, website ini cukp di akses melalui browser.

Fungsi Website
Fungsi utama dari website adalah untuk menyampaikan informasi. Dijaman modern ini website bisa digunakan sebagai platform pemasaran oleh pihak bisnis dengan menjangkau pelanggan yang lebih luas di Internet. Bagi orang pribadi, website sering dijadikan sebagai sarana komunikasi, penyebaran informasi, dan juga bisnis online. Adapun beberapa fungsi lain dari website adalah:

1. Komunikasi
Komunikasi dapat dilakukan dengan mudah melalui platform media sosial melalui website. Contohnya Facebook, WhatsApp, Twitter, Instagram dan lain sebagainya.

2. Informasi
Website berfungsi sebagai media informasi terbaru dan menarik untuk dibaca oleh pengguna internet. Selain website juga dijadikan sebagai sarana edukasi seperti tutorial, tips dan trik dan lain-lain.

3. Hiburan
Terdapat banyak sekali situs game online, film, musik dan sebagainya dalam website yang dapat menjadi hiburan bagi pengguna internet.

4. Transaksi Jual beli Online
Website juga dapat dijadikan sarana transaksi bisnis berupa barang, jasa dan sebagainya. Fungsi Transaksi ini juga dapat menghubungkan perusahaan, konsumen dan komunitas tertentu.</p>

