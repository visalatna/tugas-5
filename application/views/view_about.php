    <h1>Sejarah Website

Website pertama kali ditemukan ditemukan oleh seorang ilmuan asal Inggris yang bernaman Tim Berners-Lee, ia juga merupakan anak dari seorang ilmuan kumputer pada era awal dunia komputasi.

Tim Berners-Lee membuat website dengan tujuan untuk memudahkan para peneliti untuk bertukar informasi di tempat kerjanya. Sebelum tahun 1990, Tim Berners-Lee menuliskan tentang tiga teknologi dasar website yaitu:

• HTML (Hyper Text Markup Language) yang merupakan bahasa markup atau format untuk halaman web.
• URI (Uniform Resource Identifier) merupakan sebuah alamat unik untuk membuka halaman situs. URI ini berfungsi untuk mengidentifikasi sumber daya yang ada pada web. URI saat ini sering di sebut dengan URL (Uniform Resource Locator)
• HTTP (Hyper Text Transfer Protocol) yang memungkinkan seseorang untuk mengambil kembali sumber daya yang terkoneksi dengan semua situs web.
Pada tanggal 30 april 1993, CERN yang adalah laboratorium fisika di Swiss, dengan resmi menyatakan perilisan website secara gratis</h1>
